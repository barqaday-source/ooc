-- يأخذ أول مستخدم موجود ليجعله مالك الغرف التجريبية
do $$
declare
  v_owner uuid;
begin
  select id into v_owner from auth.users order by created_at limit 1;

  if v_owner is null then
    raise notice '⚠️ لا يوجد مستخدمون - سجّل حساب أولاً ثم أعد تشغيل هذا الملف';
    return;
  end if;

  -- غرف تجريبية (محددة بـ tag في الوصف لسهولة الحذف)
  insert into public.rooms (name, description, cover_url, owner_id) values
    ('الغرفة العامة',     '[SEED] غرفة دردشة عامة لكل المستخدمين', null, v_owner),
    ('غرفة المطورين',     '[SEED] للنقاش التقني والبرمجة',         null, v_owner),
    ('غرفة الترحيب',      '[SEED] رحّب بالأعضاء الجدد هنا',        null, v_owner)
  on conflict do nothing;
end $$;

